defined('_VALID_MOS') or die();

/**
 * @package Joostina
 * @subpackage Users
 */
class TOOLBAR_users {
	/**
	 * Draws the menu to edit a user
	 */
	public static function _EDIT() {
		global $id;

		mosMenuBar::startTable();
		mosMenuBar::save();
		mosMenuBar::spacer();
		mosMenuBar::apply();
		mosMenuBar::spacer();
		if($id) {
			// for existing content items the button is renamed `close`
			mosMenuBar::cancel('cancel',_CLOSE);
		} else {
			mosMenuBar::cancel();
		}
		mosMenuBar::spacer();
		mosMenuBar::help('screen.users');
		mosMenuBar::endTable();
	}

	public static function _CONFIG() {
		mosMenuBar::startTable();
		mosMenuBar::save('save_config');
		mosMenuBar::spacer();
		mosMenuBar::cancel();
		mosMenuBar::endTable();
	}

	public static function _DEFAULT() {
		mosMenuBar::startTable();
		mosMenuBar::custom('logout','-cancel','',_DISABLE);
		mosMenuBar::spacer();
		mosMenuBar::custom('block','-cancel','',_BLOCK_USER);
		mosMenuBar::spacer();
		mosMenuBar::custom('unblock','-publish','',_CHECKIN_OJECT);
		mosMenuBar::spacer();
		mosMenuBar::deleteList();
		mosMenuBar::spacer();
		mosMenuBar::editListX();
		mosMenuBar::spacer();
		mosMenuBar::addNewX();
		mosMenuBar::spacer();
		mosMenuBar::help('screen.users');
		mosMenuBar::endTable();
	}
}
